    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
       //Obrir el fitxer
        JFileChooser chooser = new JFileChooser();
        chooser.setCurrentDirectory(new java.io.File("."));
        chooser.setDialogTitle("Selecciona una imatge ...");
        chooser.setFileSelectionMode(JFileChooser.FILES_ONLY);          
        
        FileFilter filter = new FileFilter() {
            public boolean accept(File pathname) {
                String name = pathname.getAbsolutePath();
                return  name.endsWith(".jpg") || name.endsWith(".gif") || name.endsWith(".png")  ;
            }

            @Override
            public String getDescription() {
                return "Fitxers d'imatge";
            }
        };
          
          chooser.setFileFilter(filter);
        	          
	 if (chooser.showOpenDialog(this) == JFileChooser.APPROVE_OPTION) {
//        
          double ratio = jLabel3.getHeight() / (1.*jLabel3.getWidth());
          try {
                file = chooser.getSelectedFile();
                BufferedImage img = ImageIO.read(file);
                int w = 1;
                int h = 1;
                if(img.getWidth()>=img.getHeight()) //apaisada
                {
                    h = img.getHeight();
                    w = (int) (h / ratio);
                }
                else
                {
                    w = img.getWidth();
                    h = (int) (w* ratio);
                }
                
                Image scaledInstance = img.getSubimage(0, 0, w, h).getScaledInstance(-1, jLabel3.getHeight(), Image.SCALE_SMOOTH);
                jLabel4.setIcon(new ImageIcon(scaledInstance));
          } catch (IOException e) {
          }

         }  
          
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed

         if(file==null) {
            return;
        }
        
         int maxHeight = 70; //Evita fotos massa grans a la base de dades
         
         double ratio = jLabel3.getHeight() / (1.*jLabel3.getWidth());
         try {               
                BufferedImage img = ImageIO.read(file);
                int w = 1;
                int h = 1;
                if(img.getWidth()>=img.getHeight()) //apaisada
                {
                    h = img.getHeight();
                    w = (int) (h / ratio);
                }
                else
                {
                    w = img.getWidth();
                    h = (int) (w* ratio);
                }
                
                Image scaledInstance = img.getSubimage(0, 0, w, h).getScaledInstance(-1, maxHeight, Image.SCALE_SMOOTH);
                BufferedImage buffered = toBufferedImage(scaledInstance);
                ByteArrayOutputStream baos = new ByteArrayOutputStream();
                ImageIO.write(buffered, "jpg", baos);
		baos.flush();
                byte[] data = baos.toByteArray();
		baos.close();
                
                String SQL2 = "UPDATE `"+CoreCfg.core_mysqlDBPrefix+"`.xes_alumne_detall SET Foto=? WHERE Exp_FK_ID="+exp2;
            
                Connection con = cfg.getCoreCfg().getMysql().getConnection();
                //con.setAutoCommit(false);
                PreparedStatement ps = con.prepareStatement(SQL2); 
                ps.setBytes(1, data);
                int s = ps.executeUpdate();
                    

                
          } catch (SQLException ex) {
                Logger.getLogger(CanviaFoto.class.getName()).log(Level.SEVERE, null, ex);
            } catch (IOException e) {
          }
        this.dispose();
    }//GEN-LAST:event_jButton3ActionPerformed

     private static BufferedImage toBufferedImage(Image src) {
        int w = src.getWidth(null);
        int h = src.getHeight(null);
        int type = BufferedImage.TYPE_INT_RGB;  // other options
        BufferedImage dest = new BufferedImage(w, h, type);
        Graphics2D g2 = dest.createGraphics();
        g2.drawImage(src, 0, 0, null);
        g2.dispose();
        return dest;
    }
 
